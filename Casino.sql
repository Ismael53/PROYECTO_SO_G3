DROP DATABASE IF EXISTS Casino;
CREATE DATABASE Casino;

USE Casino;

CREATE TABLE JUGADORES (
    NOMBRE VARCHAR(60) NOT NULL,
    PASSWD VARCHAR(60) NOT NULL ,
    EDAD INT NOT NULL,
    TOTAL_POINTS INT,
    DEPOSITO INT,
    TOTAL_WINS INT,
    PRIMARY KEY (NOMBRE)

)ENGINE=InnoDB;  

CREATE TABLE PARTIDAS (
    ID INT NOT NULL,
    FECHA VARCHAR(60) NOT NULL,
    NPLAYERS INT NOT NULL,
    WINNER VARCHAR(60),
    HORA VARCHAR(60) NOT NULL,
    STATE INT NOT NULL,
    PRIMARY KEY (ID)
)ENGINE=InnoDB;  

CREATE TABLE ESTADISTICAS (
    NOMBRE VARCHAR(60) NOT NULL,
    ID_P INT NOT NULL,
    TOTAL_POINTS INT NOT NULL,
    POSITION INT NOT NULL,
    FOREIGN KEY (NOMBRE) REFERENCES JUGADORES (NOMBRE),
    FOREIGN KEY (ID_P) REFERENCES PARTIDAS (ID)
)ENGINE=InnoDB;  

