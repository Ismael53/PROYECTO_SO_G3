USE T3_BBDDCasino;

CREATE TABLE JUGADORES (
    NOMBRE VARCHAR(60) NOT NULL,
    PASSWD VARCHAR(60) NOT NULL ,
    DEPOSITO INT,
    CARGO INT,
    BENEFICIOS INT,
    APOSTADO INT,
    PRIMARY KEY (NOMBRE)

)ENGINE=InnoDB; 

CREATE TABLE APUESTAS (
    NOMBRE VARCHAR (60) NOT NULL,
    JUEGO INT NOT NULL,
    PARTIDA INT,
    RONDA INT,
    APUESTA VARCHAR (60),
    CANTIDAD INT,
    NUMERO_GANADOR INT,
    PREMIADA VARCHAR(10),
    BENEFICIO INT,
    FOREIGN KEY (NOMBRE) REFERENCES JUGADORES (NOMBRE)
)ENGINE=InnoDB;  
